function checkProbabilityTheory(count) {
    let evenCount = 0; // Лічильник парних чисел
    let oddCount = 0; // Лічильник непарних чисел
  
    // Генеруємо випадкові числа та обчислюємо кількість парних та непарних чисел
    for (let i = 0; i < count; i++) {
      const randomNum = Math.floor(Math.random() * (1000 - 100 + 1)) + 100; // Генеруємо випадкове число в діапазоні від 100 до 1000
      if (randomNum % 2 === 0) {
        evenCount++;
      } else {
        oddCount++;
      }
    }
  
    // Обчислюємо відсоткове співвідношення
    const evenPercentage = (evenCount / count) * 100;
    const oddPercentage = (oddCount / count) * 100;
  
    // Виводимо інформацію
    console.log("Кількість згенерованих чисел:", count);
    console.log("Парних чисел:", evenCount);
    console.log("Не парних чисел:", oddCount);
    console.log("Відсоток парних до не парних:", evenPercentage.toFixed(2) + "% : " + oddPercentage.toFixed(2) + "%");
  }
  
  // Приклад використання:
  checkProbabilityTheory(1000);
  
